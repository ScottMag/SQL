-- 1. FROM THE FOLLOWING TABLE WRITE A QUERY IN SQL TO RETRIEVE ALL ROWS AND COLUMNS 
-- FROM THE EMPLOYEE TABLE IN THE ADVENTUREWORKS DATABASE. SORT THE RESULT SET IN 
-- ASCENDING ORDER ON JOBTITLE. 

SELECT *
FROM HUMANRESOURCES.EMPLOYEE
ORDER BY JOBTITLE ASC;

--------------------------------------------------------------------------------------
-- 2. FROM THE FOLLOWING TABLE WRITE A QUERY IN SQL TO RETRIEVE ALL ROWS AND COLUMNS 
-- FROM THE EMPLOYEE TABLE USING TABLE ALIASING IN THE ADVENTUREWORKS DATABASE. 
-- SORT THE OUTPUT IN ASCENDING ORDER ON LASTNAME. 

SELECT *
FROM PERSON.PERSON
ORDER BY LASTNAME ASC;

--------------------------------------------------------------------------------------
-- 3. FROM THE FOLLOWING TABLE WRITE A QUERY IN SQL TO RETURN ALL ROWS AND A SUBSET OF 
-- THE COLUMNS (FIRSTNAME, LASTNAME, BUSINESSENTITYID) FROM THE PERSON TABLE IN THE 
-- ADVENTUREWORKS DATABASE. THE THIRD COLUMN HEADING IS RENAMED TO EMPLOYEE_ID. 
-- ARRANGED THE OUTPUT IN ASCENDING ORDER BY LASTNAME. 

SELECT FIRSTNAME, 
       LASTNAME, 
	   BUSINESSENTITYID AS EMPLOYEE_ID
FROM PERSON.PERSON
ORDER BY LASTNAME ASC;

--------------------------------------------------------------------------------------
-- 4. FROM THE FOLLOWING TABLE WRITE A QUERY IN SQL TO RETURN ONLY THE ROWS FOR PRODUCT 
-- THAT HAVE A SELLSTARTDATE THAT IS NOT NULL AND A PRODUCTLINE OF 'T'. RETURN PRODUCTID, 
-- PRODUCTNUMBER, AND NAME. ARRANGED THE OUTPUT IN ASCENDING ORDER ON NAME. 

SELECT PRODUCTID,
	   PRODUCTNUMBER,
	   NAME AS PRODUCTNAME
FROM PRODUCTION.PRODUCT
WHERE SELLSTARTDATE IS NOT NULL
AND   PRODUCTLINE = 'T'
ORDER BY NAME ASC;

--------------------------------------------------------------------------------------
-- 5. FROM THE FOLLOWING TABLE WRITE A QUERY IN SQL TO RETURN ALL ROWS FROM THE 
-- SALESORDERHEADER TABLE IN ADVENTUREWORKS DATABASE AND CALCULATE THE PERCENTAGE OF TAX 
-- ON THE SUBTOTAL HAVE DECIDED. RETURN SALESORDERID, CUSTOMERID, ORDERDATE, SUBTOTAL, 
-- PERCENTAGE OF TAX COLUMN. ARRANGED THE RESULT SET IN ASCENDING ORDER ON SUBTOTAL. 

SELECT SALESORDERID,
	   CUSTOMERID,
	   ORDERDATE,
	   SUBTOTAL,
	   100.00 * SUM(TAXAMT) / SUM(SUBTOTAL) AS TAX_PERCENT
FROM SALES.SALESORDERHEADER
GROUP BY SALESORDERID,
	     CUSTOMERID,
	     ORDERDATE,
	     SUBTOTAL
ORDER BY SUBTOTAL DESC;

--------------------------------------------------------------------------------------
-- 6. FROM THE FOLLOWING TABLE WRITE A QUERY IN SQL TO CREATE A LIST OF UNIQUE JOBTITLES 
-- IN THE EMPLOYEE TABLE IN ADVENTUREWORKS DATABASE. RETURN JOBTITLE COLUMN AND ARRANGED 
-- THE RESULTSET IN ASCENDING ORDER. 

SELECT JOBTITLE
FROM HUMANRESOURCES.EMPLOYEE
GROUP BY JOBTITLE
ORDER BY JOBTITLE ASC;

--------------------------------------------------------------------------------------
-- 7. FROM THE FOLLOWING TABLE WRITE A QUERY IN SQL TO CALCULATE THE TOTAL FREIGHT PAID 
-- BY EACH CUSTOMER. RETURN CUSTOMERID AND TOTAL FREIGHT. SORT THE OUTPUT IN ASCENDING 
-- ORDER ON CUSTOMERID. 

SELECT CUSTOMERID,
	   SUM(FREIGHT)
FROM SALES.SALESORDERHEADER
GROUP BY CUSTOMERID
ORDER BY CUSTOMERID ASC;

--------------------------------------------------------------------------------------
-- 8. FROM THE FOLLOWING TABLE WRITE A QUERY IN SQL TO FIND THE AVERAGE AND THE SUM OF 
-- THE SUBTOTAL FOR EVERY CUSTOMER. RETURN CUSTOMERID, AVERAGE AND SUM OF THE SUBTOTAL.
-- GROUPED THE RESULT ON CUSTOMERID AND SALESPERSONID. SORT THE RESULT ON CUSTOMERID 
-- COLUMN IN DESCENDING ORDER. 

SELECT CUSTOMERID,
	   SALESPERSONID,
	   AVG(SUBTOTAL) AS AVG_SUBTOTAL,
	   SUM(SUBTOTAL) AS SUM_SUBTOTAL
FROM SALES.SALESORDERHEADER
GROUP BY CUSTOMERID,
	     SALESPERSONID
ORDER BY CUSTOMERID DESC;

--------------------------------------------------------------------------------------
-- 9. FROM THE FOLLOWING TABLE WRITE A QUERY IN SQL TO RETRIEVE TOTAL QUANTITY OF EACH 
-- PRODUCTID WHICH ARE IN SHELF OF 'A' OR 'C' OR 'H'. FILTER THE RESULTS FOR SUM QUANTITY 
-- IS MORE THAN 500. RETURN PRODUCTID AND SUM OF THE QUANTITY. SORT THE RESULTS ACCORDING 
-- TO THE PRODUCTID IN ASCENDING ORDER. 

SELECT PRODUCTID,
	   SUM(QUANTITY) AS TOTAL_QUANTITY
FROM PRODUCTION.PRODUCTINVENTORY
WHERE SHELF IN ('A', 'C', 'H')
GROUP BY PRODUCTID;

--------------------------------------------------------------------------------------
-- 10. FROM THE FOLLOWING TABLE WRITE A QUERY IN SQL TO FIND THE TOTAL QUENTITY FOR A 
-- GROUP OF LOCATIONID MULTIPLIED BY 10. 

SELECT SUM(QUANTITY) AS TOTAL_QUANTITY
FROM PRODUCTION.PRODUCTINVENTORY
GROUP BY (LOCATIONID * 10);

--------------------------------------------------------------------------------------
-- 11. FROM THE FOLLOWING TABLES WRITE A QUERY IN SQL TO FIND THE PERSONS WHOSE LAST NAME 
-- STARTS WITH LETTER 'L'. RETURN BUSINESSENTITYID, FIRSTNAME, LASTNAME, AND PHONENUMBER. 
-- SORT THE RESULT ON LASTNAME AND FIRSTNAME. 

SELECT PP.BUSINESSENTITYID,
       P.FIRSTNAME,
	   P.LASTNAME,
	   PP.PHONENUMBER
FROM PERSON.PERSONPHONE PP
INNER JOIN PERSON.PERSON P
ON PP.BUSINESSENTITYID = P.BUSINESSENTITYID
WHERE P.LASTNAME LIKE 'L%'
ORDER BY P.LASTNAME, P.FIRSTNAME;

--------------------------------------------------------------------------------------
-- 12. FROM THE FOLLOWING TABLE WRITE A QUERY IN SQL TO FIND THE SUM OF SUBTOTAL COLUMN. 
-- GROUP THE SUM ON DISTINCT SALESPERSONID AND CUSTOMERID. ROLLS UP THE RESULTS INTO 
-- SUBTOTAL AND RUNNING TOTAL. RETURN SALESPERSONID, CUSTOMERID AND SUM OF SUBTOTAL 
-- COLUMN I.E. SUM_SUBTOTAL. 

SELECT SALESPERSONID,
	   CUSTOMERID,
	   SUM(SUBTOTAL) AS SUM_SUBTOTAL
FROM SALES.SALESORDERHEADER
WHERE SALESPERSONID IS NOT NULL
GROUP BY ROLLUP(SALESPERSONID,
	            CUSTOMERID);

--------------------------------------------------------------------------------------
-- 13. FROM THE FOLLOWING TABLE WRITE A QUERY IN SQL TO FIND THE SUM OF THE QUANTITY OF 
-- ALL COMBINATION OF GROUP OF DISTINCT LOCATIONID AND SHELF COLUMN. RETURN LOCATIONID, 
-- SHELF AND SUM OF QUANTITY AS TOTALQUANTITY. 

SELECT LOCATIONID,
	   SHELF,
	   SUM(QUANTITY) AS TOTALQUANTITY
FROM PRODUCTION.PRODUCTINVENTORY
GROUP BY CUBE(LOCATIONID,
	            SHELF);

--------------------------------------------------------------------------------------
-- 14. FROM THE FOLLOWING TABLE WRITE A QUERY IN SQL TO FIND THE SUM OF THE QUANTITY WITH 
-- SUBTOTAL FOR EACH LOCATIONID. GROUP THE RESULTS FOR ALL COMBINATION OF DISTINCT 
-- LOCATIONID AND SHELF COLUMN. ROLLS UP THE RESULTS INTO SUBTOTAL AND RUNNING TOTAL. 
-- RETURN LOCATIONID, SHELF AND SUM OF QUANTITY AS TOTALQUANTITY. 

SELECT LOCATIONID,
	   SHELF,
	   SUM(QUANTITY) AS TOTALQUANTITY
FROM PRODUCTION.PRODUCTINVENTORY
GROUP BY GROUPING SETS (ROLLUP (LOCATIONID, SHELF), CUBE (LOCATIONID, SHELF));

--------------------------------------------------------------------------------------
-- 15. FROM THE FOLLOWING TABLE WRITE A QUERY IN SQL TO FIND THE TOTAL QUANTITY FOR EACH 
-- LOCATIONID AND CALCULATE THE GRAND-TOTAL FOR ALL LOCATIONS. RETURN LOCATIONID AND 
-- TOTAL QUANTITY. GROUP THE RESULTS ON LOCATIONID. 

SELECT LOCATIONID,
	   SUM(QUANTITY) AS TOTALQUANTITY
FROM PRODUCTION.PRODUCTINVENTORY
GROUP BY ROLLUP(LOCATIONID);

--------------------------------------------------------------------------------------
-- 16. FROM THE FOLLOWING TABLE WRITE A QUERY IN SQL TO RETRIEVE THE NUMBER OF EMPLOYEES 
-- FOR EACH CITY. RETURN CITY AND NUMBER OF EMPLOYEES. SORT THE RESULT IN ASCENDING ORDER 
-- ON CITY.

SELECT A.CITY,
	   COUNT(DISTINCT BUSINESSENTITYID) AS NOOFEMPLOYEES
FROM PERSON.BUSINESSENTITYADDRESS BEA
INNER JOIN PERSON.ADDRESS A
ON BEA.ADDRESSID = A.ADDRESSID
GROUP BY A.CITY
ORDER BY A.CITY ASC;

--------------------------------------------------------------------------------------
-- 17. FROM THE FOLLOWING TABLE WRITE A QUERY IN SQL TO RETRIEVE THE TOTAL SALES FOR EACH 
-- YEAR. RETURN THE YEAR PART OF ORDER DATE AND TOTAL DUE AMOUNT. SORT THE RESULT IN 
-- ASCENDING ORDER ON YEAR PART OF ORDER DATE. 

SELECT YEAR(OrderDate) AS YEAR,
	   SUM(TOTALDUE) AS "Order Amount"
FROM Sales.SalesOrderHeader
GROUP BY YEAR(OrderDate)
ORDER BY YEAR(OrderDate) ASC;

--------------------------------------------------------------------------------------
-- 18. FROM THE FOLLOWING TABLE WRITE A QUERY IN SQL TO RETRIEVE THE TOTAL SALES FOR EACH 
-- YEAR. FILTER THE RESULT SET FOR THOSE ORDERS WHERE ORDER YEAR IS ON OR BEFORE 2016. 
-- RETURN THE YEAR PART OF ORDERDATE AND TOTAL DUE AMOUNT. SORT THE RESULT IN ASCENDING 
-- ORDER ON YEAR PART OF ORDER DATE. 

SELECT YEAR(OrderDate) AS YEARFORORDERDATE,
	   SUM(TOTALDUE)   AS TOTALDUEORDER
FROM Sales.SalesOrderHeader
GROUP BY YEAR(OrderDate)
HAVING   YEAR(OrderDate) <= '2016'
ORDER BY YEAR(OrderDate) ASC;

--------------------------------------------------------------------------------------
-- 19. FROM THE FOLLOWING TABLE WRITE A QUERY IN SQL TO FIND THE CONTACTS WHO ARE 
-- DESIGNATED AS A MANAGER IN VARIOUS DEPARTMENTS. RETURNS CONTACTTYPEID, NAME. SORT THE 
-- RESULT SET IN DESCENDING ORDER. 

SELECT ContactTypeID,
	   NAME
FROM Person.ContactType
WHERE UPPER(NAME) LIKE '%MANAGER%'
ORDER BY ContactTypeID DESC;

--------------------------------------------------------------------------------------
-- 20. FROM THE FOLLOWING TABLES WRITE A QUERY IN SQL TO MAKE A LIST OF CONTACTS WHO ARE 
-- DESIGNATED AS 'PURCHASING MANAGER'. RETURN BUSINESSENTITYID, LASTNAME, AND FIRSTNAME 
-- COLUMNS. SORT THE RESULT SET IN ASCENDING ORDER OF LASTNAME, AND FIRSTNAME. 

SELECT BEC.BusinessEntityID,
	   P.LastName,
	   P.FirstName
FROM Person.BusinessEntityContact BEC
INNER JOIN PERSON.ContactType CT
ON BEC.ContactTypeID = CT.ContactTypeID
AND UPPER(CT.Name) = 'PURCHASING MANAGER'
INNER JOIN PERSON.Person P
ON BEC.PersonID = P.BusinessEntityID
ORDER BY LastName, FIRSTNAME;

