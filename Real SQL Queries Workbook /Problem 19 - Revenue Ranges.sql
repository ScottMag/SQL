SELECT DISTINCT CASE WHEN SOH.TOTALDUE < 100    THEN '1'
					 WHEN SOH.TOTALDUE < 500    THEN '2'
					 WHEN SOH.TOTALDUE < 1000   THEN '3'
					 WHEN SOH.TOTALDUE < 2500   THEN '4'
					 WHEN SOH.TOTALDUE < 5000   THEN '5'
					 WHEN SOH.TOTALDUE < 10000  THEN '6'
					 WHEN SOH.TOTALDUE < 50000  THEN '7'
					 WHEN SOH.TOTALDUE < 100000 THEN '8'
					 ELSE '9'
				END AS SORT_ID,
				CASE WHEN SOH.TOTALDUE < 100    THEN '$0 - $100'
					 WHEN SOH.TOTALDUE < 500    THEN '$100 - $500'
					 WHEN SOH.TOTALDUE < 1000   THEN '$500 - $1,000'
					 WHEN SOH.TOTALDUE < 2500   THEN '$1,000 - $2,500'
					 WHEN SOH.TOTALDUE < 5000   THEN '$2,500 - $5,000'
					 WHEN SOH.TOTALDUE < 10000  THEN '$5,000 - $10,000'
					 WHEN SOH.TOTALDUE < 50000  THEN '$10,000 - $50,000'
					 WHEN SOH.TOTALDUE < 100000 THEN '$50,000 - $100,000'
					 ELSE '> $100,000'
				END AS SALES_AMOUNT_CATEGORY,
				COUNT(*) AS ORDERS
FROM SALES.SALESORDERHEADER SOH
WHERE YEAR(ORDERDATE) = 2014
GROUP BY CASE WHEN SOH.TOTALDUE < 100    THEN '1'
			  WHEN SOH.TOTALDUE < 500    THEN '2'
			  WHEN SOH.TOTALDUE < 1000   THEN '3'
			  WHEN SOH.TOTALDUE < 2500   THEN '4'
			  WHEN SOH.TOTALDUE < 5000   THEN '5'
			  WHEN SOH.TOTALDUE < 10000  THEN '6'
			  WHEN SOH.TOTALDUE < 50000  THEN '7'
			  WHEN SOH.TOTALDUE < 100000 THEN '8'
			  ELSE '9'
	     END,
		 CASE WHEN SOH.TOTALDUE < 100    THEN '$0 - $100'
			  WHEN SOH.TOTALDUE < 500    THEN '$100 - $500'
			  WHEN SOH.TOTALDUE < 1000   THEN '$500 - $1,000'
			  WHEN SOH.TOTALDUE < 2500   THEN '$1,000 - $2,500'
			  WHEN SOH.TOTALDUE < 5000   THEN '$2,500 - $5,000'
			  WHEN SOH.TOTALDUE < 10000  THEN '$5,000 - $10,000'
			  WHEN SOH.TOTALDUE < 50000  THEN '$10,000 - $50,000'
			  WHEN SOH.TOTALDUE < 100000 THEN '$50,000 - $100,000'
			  ELSE '> $100,000'
		 END
ORDER BY CASE WHEN SOH.TOTALDUE < 100    THEN '1'
			  WHEN SOH.TOTALDUE < 500    THEN '2'
			  WHEN SOH.TOTALDUE < 1000   THEN '3'
			  WHEN SOH.TOTALDUE < 2500   THEN '4'
			  WHEN SOH.TOTALDUE < 5000   THEN '5'
			  WHEN SOH.TOTALDUE < 10000  THEN '6'
			  WHEN SOH.TOTALDUE < 50000  THEN '7'
			  WHEN SOH.TOTALDUE < 100000 THEN '8'
			  ELSE '9'
	     END
